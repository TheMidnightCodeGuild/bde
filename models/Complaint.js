const mongoose = require('mongoose');

const complaintSchema = new mongoose.Schema({
  intern: {
    type: String,
    required: true
  },
  title: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.models.Complaint || mongoose.model('Complaint', complaintSchema);
